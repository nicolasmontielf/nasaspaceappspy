<p>Along with fellow dwarf planets Pluto, Eris and Haumea, Makemake is located in the Kuiper Belt, a region outside the orbit of Neptune. Slightly smaller than Pluto, Makemake is the second-brightest object in the Kuiper Belt as seen from Earth (while Pluto is the brightest). It takes about 305 Earth years for this dwarf planet to make one trip around the sun.</p>
<p>Makemake holds an important place in the history of solar system studies because it—along with Eris—was one of the objects whose discovery prompted the International Astronomical Union to reconsider the definition of a planet and to create the new group of dwarf planets.</p>
<p>Makemake was named after the Rapanui god of fertility.</p>
<p><strong>Discovery</strong></p>
<p>Makemake was discovered Mar. 31, 2005 by M.E. Brown, C.A. Trujillo, and D. Rabinowitz at the Palomar Observatory.</p>
<p><strong>Size and Distance</strong></p>
<p>With a radius of approximately 444 miles (715 kilometers), Makemake is 1/9 the radius of Earth. If Earth were the size of a nickel, Makemake would be about as big as a mustard seed.</p>
<p>From an average distance of 4,253,000,000 miles (6,847,000,000 kilometers), Makemake is 45.8 astronomical units away from the sun. One astronomical unit (abbreviated as AU), is the distance from the sun to Earth. From this distance, it takes sunlight 6 hours and 20 minutes to travel from the sun to Makemake.</p>
<p><strong>Orbit and Rotation</strong></p>
<p>Makemake takes 305 Earth years to make one trip around the sun. As Makemake orbits the sun, it completes one rotation every 22 and a half hours, making its day length similar to Earth and Mars.</p>
<p><strong>Formation</strong></p>
<p>Dwarf planet Makemake is a member of a group of objects that orbit in a disc-like zone beyond the orbit of Neptune called the Kuiper Belt. This distant realm is populated with thousands of miniature icy worlds, which formed early in the history of our solar system about 4.5 billion years ago. These icy, rocky bodies are called Kuiper Belt objects, transneptunian objects, or plutoids.</p>
<p><strong>Structure</strong></p>
<p>Scientists know very little about Makemake's structure.</p>
<p><strong>Surface</strong></p>
<p>We can't see too many details of Makemake's surface from so far away, but it does appear to be a reddish-brownish color, similar to Pluto. Scientists have also detected frozen methane and ethane on its surface. In fact, pellets of frozen methane as big as half an inch (1 centimeter) in diameter may rest on Makemake's cold surface.</p>
<p><strong>Atmosphere</strong></p>
<p>Makemake may develop a very thin atmosphere, most likely made of nitrogen, near perihelion — when it is closest to the sun.</p>
<p><strong>Potential for Life</strong></p>
<p>The surface of Makemake is extremely cold, so it seems unlikely that life could exist there.</p>
<p><strong>Moons</strong></p>
<p>Makemake has one provisional moon, S/2015 (136472) 1 and nicknamed MK 2. It is more than 1,300 times fainter than Makemake. MK 2 was seen approximately 13,000 miles from the dwarf planet, and its radius is estimated to be about 50 miles (80 kilometers).</p>
<p><strong>Rings</strong></p>
<p>There are no known rings around Makemake.</p>
<p><strong>Magnetosphere</strong></p>
<p>Scientists do not know if Makemake has a magnetosphere.</p>
<p><strong>Exploration</strong></p>
<p>Makemake was first observed in March 2005 by M.E. Brown, C.A. Trujillo, and D.L. Rabinowitz at the Palomar Observatory. Its unofficial codename was Easterbunny, and before this dwarf planet was confirmed, its provisional name was 2005 FY9.</p>
<p><strong>Significant Dates</strong></p>
<ul>
<li><strong>2005:</strong> Makemake is first observed with ground-based telescopes at the Palomar Observatory.</li>
<li><strong>2008:</strong> Makemake recognized as a dwarf planet by the International  Astronomical Union.</li>
<li><strong>2016:</strong> NASA’s Hubble Space Telescope has spotted a small, dark moon orbiting Makemake, the second brightest icy dwarf planet—after Pluto—in the Kuiper Belt.</li>
</ul>

